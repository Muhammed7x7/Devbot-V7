@bot.event
async def on_interaction(interaction: discord.Interaction):
    if interaction.type == discord.InteractionType.component:
        custom_id = interaction.data.get("custom_id", "")
        
        if not check_owner(interaction):
            await interaction.response.send_message("вқҢ Bu bot geliЕҹtirici iГ§in!", ephemeral=True)
            return
        
        # MenГј butonlarДұ
        if custom_id == "menu_image":
            await interaction.response.send_modal(ImageModal())
        elif custom_id == "menu_chat":
            modal = Modal(title="рҹ’¬ HДұzlДұ Sohbet")
            msg_input = TextInput(label="MesajДұn", style=discord.TextStyle.paragraph, required=True)
            modal.add_item(msg_input)
            
            async def modal_submit(m_interaction):
                await devchat_command(m_interaction, msg_input.value)
            
            modal.on_submit = modal_submit
            await interaction.response.send_modal(modal)
        elif custom_id == "menu_stats":
            await devstats_command(interaction)
        
        # GГ¶rsel butonlarДұ
        elif custom_id.startswith("resize_"):
            prompt = custom_id[7:]
            select = Select(
                placeholder="Boyut seГ§",
                options=[
                    discord.SelectOption(label="Kare (1024x1024)", value="1024x1024", emoji="в¬ӣ"),
                    discord.SelectOption(label="Yatay (1792x1024)", value="1792x1024", emoji="рҹҹҰ"),
                    discord.SelectOption(label="Dikey (1024x1792)", value="1024x1792", emoji="рҹҹ©"),
                ]
            )
            
            async def select_callback(s_interaction):
                await image_command(s_interaction, prompt, select.values[0])
            
            select.callback = select_callback
            view = View()
            view.add_item(select)
            await interaction.response.send_message("рҹ“җ **Yeni boyut seГ§:**", view=view, ephemeral=True)
        
        elif custom_id.startswith("regenerate_"):
            prompt = custom_id[11:]
            await image_command(interaction, prompt, "1024x1024")

